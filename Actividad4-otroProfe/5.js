let dia
while(!(dia=="domingo")){
    dia = prompt("Ingrese un día de la semana")
    if(dia=="lunes"){
        alert("buen inicio de semana")
    }else if(dia == "martes"){
        alert("Que tal si aprendemos algo nuevo?")
    }else if(dia =="miercoles"){
        alert("ombligo de semana")
    }else if(dia =="jueves"){
        alert("vamo a calmarnos")
    }else if(dia =="viernes"){
        alert("es viernes y el cuerpo lo sabe")
    }else if(dia =="sabado"){
        alert("dia de cruda")
    }else if(dia=="domingo"){
        alert("Ve a descansar")
    }
    else{
        alert("Ingrese un día valido")
    }
}
